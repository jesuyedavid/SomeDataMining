type DataAndAlgo struct {
	algoName string
	algoData []float64
}

func hitAndMiss(f *os.File, algo DataAndAlgo) {
	var hit, miss int
	f.WriteString("\nHit and Miss Data for " + algo.algoName + "\nCW\t\tH\t\tM")

	for _, i := range algo.algoData {
		if i <= 5 {
			hit++
		} else {
			miss++
		}
	}

	f.WriteString("\n" + strconv.Itoa(5) + "\t\t" + strconv.Itoa(hit) + "\t\t" + strconv.Itoa(miss))
	hit = 0
	miss = 0

	for _, i := range algo.algoData {
		if i > 5 && i < 10 {
			hit++
		} else {
			miss++
		}
	}
	f.WriteString("\n" + strconv.Itoa(10) + "\t\t" + strconv.Itoa(hit) + "\t\t" + strconv.Itoa(miss))
	hit = 0
	miss = 0

	for _, i := range algo.algoData {
		if i >= 10 && i <= 15 {
			hit++
		} else {
			miss++
		}
	}
	f.WriteString("\n" + strconv.Itoa(15) + "\t\t" + strconv.Itoa(hit) + "\t\t" + strconv.Itoa(miss))
	hit = 0
	miss = 0

	for _, i := range algo.algoData {
		if i > 15 {
			hit++
		} else {
			miss++
		}
	}
	f.WriteString("\n" + strconv.Itoa(15) + "+\t\t" + strconv.Itoa(hit) + "\t\t" + strconv.Itoa(miss))
}

